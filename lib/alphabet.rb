class Alphabet
  attr_reader :braille_letter_hash, :english

  def initialize
    @braille_letter_hash = {
      "a" => "0.....",
      "b" => "0.0...",
      "c" => "00....",
      "d" => "00.0..",
      "e" => "0..0..",
      "f" => "000...",
      "g" => "0000..",
      "h" => "0.00..",
      "i" => ".00...",
      "j" => ".000..",
      "k" => "0...0.",
      "l" => "0.0.0.",
      "m" => "00..0.",
      "n" => "00.00.",
      "o" => "0..00.",
      "p" => "000.0.",
      "q" => "00000.",
      "r" => "0.000.",
      "s" => ".00.0.",
      "t" => ".0000.",
      "u" => "0...00",
      "v "=> "0.0.00",
      "w" => ".000.0",
      "x" => "00..00",
      "y" => "00.000",
      "z" => "0..000",
      " " => "......",
      :capitalize => ".....0",
      "#" => ".0.000",
      "." => "..00.0",
      "!" => "..000.",
      "'" => "....0.",
      "," => "..0...",
      "-" => "....00",
      "?" => "..0.00"
    }

    @english = {
      ["0.", "..", ".."] => "a",
      ["0.", "0.", ".."] => "b",
      ["00", "..", ".."] => "c",
      ["00", ".0", ".."] => "d",
      ["0.", ".0", ".."] => "e",
      ["00", "0.", ".."] => "f",
      ["00", "00", ".."] => "g",
      ["0.", "00", ".."] => "h",
      [".0", "0.", ".."] => "i",
      [".0", "00", ".."] => "j",
      ["0.", "..", "0."] => "k",
      ["0.", "0.", "0."] => "l",
      ["00", "..", "0."] => "m",
      ["00", ".0", "0."] => "n",
      ["0.", ".0", "0."] => "o",
      ["00", "0.", "0."] => "p",
      ["00", "00", "0."] => "q",
      ["0.", "00", "0."] => "r",
      [".0", "0.", "0."] => "s",
      [".0", "00", "0."] => "t",
      ["0.", "..", "00"] => "u",
      ["0.", "0.", "00"] => "v",
      [".0", "00", ".0"] => "w",
      ["00", "..", "00"] => "x",
      ["00", ".0", "00"] => "y",
      ["0.", ".0", "00"] => "z",
      ["..", "..", ".."] => " "
    }

    def to_english(char)
      english[(char)]
    end
  end
end

